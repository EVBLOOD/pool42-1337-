#include<unistd.h>
int	main(int argc, char **argv)
{
	while (*argv++ && argc > 1)
	{
		while (**argv)
		{
			write(1, (*argv), 1);
			(*argv)++;
		}
		write(1, "\n", 1);
		argc--;
	}
}
