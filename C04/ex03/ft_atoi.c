int	ft_cal(char a)
{
	int	k;

	k = a - '0';
	return (k);
}

int	ft_atoi(char *str)
{
	int		i;
	int		s;

	i = 0;
	s = 1;
	while (*str && (*str == ' ' || (*str >= 9 && *str <= 13)))
		str++;
	while (*str && (*str == '-' || *str == '+'))
	{
		if (*str == '-')
		{
			s *= -1;
		}
		str++;
	}
	while (*str && *str >= '0' && *str <= '9')
	{
		i *= 10;
		i += ft_cal(*str);
		str++;
	}
	i *= s;
	return (i);
}
