#include<unistd.h>
void	ft_putnbr(int nb)
{
	long	i;
	char	x;

	i = nb;
	if (i < 0)
	{
		i = i * -1;
		write(1, "-", 1);
	}
	if (i < 10)
	{
		x = '0' + i;
		write(1, &x, 1);
	}
	else
	{
		ft_putnbr(i / 10);
		ft_putnbr(i % 10);
	}
}
