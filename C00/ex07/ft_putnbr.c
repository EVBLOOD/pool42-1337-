#include<unistd.h>
void	ft_putnbr(int nbc)
{
	char	nm;
	long	nb;

	nm = '0';
	nb = nbc;
	if (nb < 0)
	{
		write(1, "-", 1);
		nb = nb * -1;
	}
	if (nb < 10)
	{
		nm += nb;
		write(1, &nm, 1);
	}
	else
	{
		 ft_putnbr(nb / 10);
		 ft_putnbr(nb % 10);
	}
}
